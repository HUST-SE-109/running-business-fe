<template>
  <div id="login-page" @keyup.enter="login">
    <div class="login-form">
      <div class="title">跑商-用户登录</div>
      <div class="input-group">
        <el-input
          placeholder="请输入手机号"
          suffix-icon="el-icon-mobile-phone"
          v-model="username"
          @blur="handleBlur"
          clearable
        >
        </el-input>
      </div>
      <div class="input-group">
        <el-input
          placeholder="请输入密码"
          suffix-icon="el-icon-message"
          type="password"
          v-model="password"
          clearable
        >
        </el-input>
      </div>
      <div class="input-group">
        <el-button
          @click.native="register" type="primary" :loading="isLoading">{{ btnText }}
        </el-button>
      </div>
    </div>
  </div>
</template>

<script>
import { isMobile, checkPassword } from '@/util/util';
import { checkUserRegistered, register } from '../../api/register';

export default {
  data() {
    return {
      username: '',
      password: '',
      isLoading: false,
      isRegistered: true,
    };
  },
  computed: {
    btnText() {
      if (this.isLoading) return '注册中...';
      return '注册';
    },
  },
  methods: {
    checkRegistered() {
      checkUserRegistered(this.username)
        .then(({ code }) => {
          if (code === '200') {
            this.isRegistered = false;
            this.$message({
              message: '恭喜您，账号可用！',
              type: 'success',
            });
          } else {
            this.isRegistered = true;
            this.$message({
              message: '用户已注册',
              type: 'warning',
            });
          }
        });
    },
    checkUserInfo() {
      if (!this.username) {
        this.$message.error('请填写手机号');
        return;
      }
      if (this.username && !isMobile(this.username)) {
        this.$message.error('请填写正确格式手机号');
        return;
      }
      if (!this.password) {
        this.$message.error('请填写密码');
        return;
      }
      if (this.password && !checkPassword(this.password)) {
        this.$message.error('密码应为6-18位');
      }
    },
    handleBlur() {
      this.checkRegistered();
    },
    register() {
      this.checkUserInfo();
      // this.isLoading = true;
      const data = {
        userphone: this.username,
        password: this.password,
      };
      register(data)
        .then(({ code }) => {
        });
